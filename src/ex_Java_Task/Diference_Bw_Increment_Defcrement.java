package ex_Java_Task;

public class Diference_Bw_Increment_Defcrement {
    public static void main(String[] args) {
        int a = 10, b = 20;

        System.out.println("before increment = " + a++ + " after increment=" + a); // output 10 and it will become 11
        System.out.println("increment value = " + ++b); // it will become 21 and print 21
    }
}
