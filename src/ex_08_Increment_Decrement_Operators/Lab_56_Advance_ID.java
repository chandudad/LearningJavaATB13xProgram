package ex_08_Increment_Decrement_Operators;

public class Lab_56_Advance_ID {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + a);
        // A + B
        //A -> a++ ->ExpA -> 10, a =11
        // +
        // B -> a ->ExpB, ExpB ->11, a=11
    }
}
