package ex_10_Switch;

public class Lab_84_Interview_07 {
    public static void main(String[] args) {
        char code = 'A';
        switch (code){
            default:
                System.out.println("Helooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
    }
    }
}
